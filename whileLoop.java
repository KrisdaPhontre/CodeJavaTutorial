public class whileLoop {
    public static void main(String[] args) {
        System.out.println("[---------------------------]");
        int iwhile = 1;
        while (iwhile <= 100) {
            System.out.println(+iwhile);
            iwhile += 1;
        
        }
        System.out.println(iwhile*50);
        System.out.println("[---------------------------]");
    }
}
